# Функция для нахождения среднего 3х чисел
def Median(int1, int2, int3):
	return ( (int1 + int2 + int3)/3 )

print( Median ( 
	int(input("Введите число 1: ")), 
	int(input("Введите число 2: ")), 
	int(input("Введите число 3: ")) 
	) )
                                                                                

